export function getConditionQuestions(specialty, conditionKey) {
  const conditions = SPECIALTY_CONDITIONS[specialty];
  if (!conditions) return null;
  return conditions[conditionKey] || null;
}

export function detectCondition(specialty, text) {
  if (!text || !SPECIALTY_CONDITIONS[specialty]) return null;
  const lower = text.toLowerCase();
  const conditions = SPECIALTY_CONDITIONS[specialty];
  let bestMatch = null;
  let maxScore = 0;
  for (const [key, condition] of Object.entries(conditions)) {
    let score = 0;
    for (const kw of condition.keywords) {
      if (lower.includes(kw)) score += kw.length;
    }
    if (score > maxScore) {
      maxScore = score;
      bestMatch = key;
    }
  }
  return maxScore > 0 ? bestMatch : null;
}

export function getAllConditionKeys(specialty) {
  const conditions = SPECIALTY_CONDITIONS[specialty];
  if (!conditions) return [];
  return Object.keys(conditions);
}

export function getConditionNames(specialty) {
  const conditions = SPECIALTY_CONDITIONS[specialty];
  if (!conditions) return [];
  return Object.entries(conditions).map(([key, c]) => ({ key, name: c.name }));
}
